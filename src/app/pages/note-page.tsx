import {
  MenuItem,
  MenuPopup,
  MenuPortal,
  MenuPositioner,
  MenuRoot,
  MenuTrigger,
  TextContent,
} from "@/app/components";
import { notesRepo } from "@/app/db";
import { EditNoteDialog } from "@/app/features/notes";
import { useLocalData } from "@/app/hooks/use-local-data";
import { navigate } from "@/app/utils/navigate";
import { CaretLeftIcon, DotsThreeIcon, PencilSimpleIcon, TrashIcon } from "@phosphor-icons/react";
import { format, parseISO } from "date-fns";
import { useState } from "react";

export function NotePage({ id }: { id: string }) {
  const note = useLocalData(() => notesRepo.findById(id), [id]);
  const [editOpen, setEditOpen] = useState(false);
  const from = new URLSearchParams(window.location.search).get("from") as "index" | "entries" | null;

  const goBack = () => {
    if (from === "entries") {
      navigate("entries", undefined, { transition: "slide-right" });
    } else {
      navigate("app", undefined, { transition: "slide-right" });
    }
  };

  const handleDelete = () => {
    if (!note) return;
    notesRepo.delete(note.id);
    goBack();
  };

  if (!note) return null;

  const formattedDate = format(parseISO(note.date), "MMMM d");
  const createdTime = format(parseISO(note.createdAt), "h:mm a");

  return (
    <div className="flex min-h-screen flex-col max-w-2xl mx-auto pt-safe-top pb-safe-bottom">
      <header className="flex items-center gap-2 px-4 py-2">
        <button
          type="button"
          onClick={goBack}
          className="flex size-10 shrink-0 items-center justify-center rounded-md transition-transform active:scale-105"
          aria-label="Go back"
        >
          <CaretLeftIcon className="size-6" />
        </button>
        <div className="flex-1 text-center flex flex-col justify-between gap-2">
          <time className="font-medium" dateTime={note.date}>
            {formattedDate}
          </time>
          <time className="block text-xs text-cloud-medium" dateTime={note.createdAt}>
            {createdTime}
          </time>
        </div>
        <MenuRoot>
          <MenuTrigger className="flex size-10 shrink-0 items-center justify-center rounded-md transition-transform active:scale-105">
            <DotsThreeIcon className="size-6" />
          </MenuTrigger>
          <MenuPortal>
            <MenuPositioner align="end">
              <MenuPopup>
                <MenuItem onClick={() => setEditOpen(true)} className="flex gap-2">
                  <PencilSimpleIcon className="size-4" />
                  Edit
                </MenuItem>
                <MenuItem onClick={handleDelete} className="text-error flex gap-2">
                  <TrashIcon className="size-4" />
                  Delete
                </MenuItem>
              </MenuPopup>
            </MenuPositioner>
          </MenuPortal>
        </MenuRoot>
      </header>
      <TextContent content={note.content} updatedAt={note.updatedAt} createdAt={note.createdAt} />
      <EditNoteDialog open={editOpen} onClose={() => setEditOpen(false)} note={note} />
    </div>
  );
}
